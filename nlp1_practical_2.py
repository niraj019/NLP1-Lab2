    # The shape of each of these is [batch_size, hidden_size]

    i = i.sigmoid()
    f_l = f_l.sigmoid ()
    f_r = f_r.sigmoid()
    g = -g.tanh()
    o = o.sigmoid()

    c = i*g + f_l*prev_c_l + f_r*prev_c_r
    h = o*c.tanh()
    
